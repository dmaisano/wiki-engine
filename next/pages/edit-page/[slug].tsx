type PageProps = {};

export const getServerSideProps: GetServerSideProps = async (context) => {
  if (!context.params || typeof context.params["slug"] !== "string") {
    return {
      notFound: true,
    };
  }

  const slug = context.params["slug"];

  let data: PageProps = { slug } as any;

  return {
    props: {
      data,
    },
  };
};

